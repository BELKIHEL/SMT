package com.SMT;

public class Conséquences {
	
	public String Accident;
	public String Cause;
	public String Conséquence;
	public String getAccident() {
		return Accident;
	}
	public void setAccident(String accident) {
		Accident = accident;
	}
	public String getCause() {
		return Cause;
	}
	public void setCause(String cause) {
		Cause = cause;
	}
	public String getConséquence() {
		return Conséquence;
	}
	public void setConséquence(String conséquence) {
		Conséquence = conséquence;
	}
	
	

}
